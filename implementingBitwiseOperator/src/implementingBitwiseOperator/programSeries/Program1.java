package implementingBitwiseOperator.programSeries;

public class Program1 {
	public static void main(String[] args) {
		int num=100;
		System.out.println("Using right shift operator "+num+" becomes "+ (num >> 1)+" when (num >> 1) is performed.");
		System.out.println("Using left shift operator "+num+" becomes "+ (num << 1)+" when (num << 1) is performed.");

	}

}
